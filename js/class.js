class Jugador{
    constructor (nombreJugador,numero,preguntaAnterior,escalonActual){
        this.nombreJugador=nombreJugador;
        this.numero=numero;
        this.preguntaAnterior=preguntaAnterior;
        this.escalonActual=escalonActual;
    }
}


class Pregunta{
    constructor(pregunta,opciones,respuesta){
        this.pregunta= pregunta;
        this.opciones=opciones;
        this.respuesta=respuesta;
    }
}




